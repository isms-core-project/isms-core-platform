def check_sheet_count(wb, expected_count=12):
    """Verify correct number of sheets."""
    actual = len(wb.sheetnames)
    if actual != expected_count:
        print(f"✗ FAIL: Expected {expected_count} sheets, found {actual}")
        return False
    print(f"✓ Sheet count correct: {actual} sheets")
    return True


def check_sheet_names(wb):
    """Verify all required sheets present."""
    required = [
        "Executive_Summary", "Program_KPIs", "Source_Portfolio",
        "Intelligence_Operations", "Integration_Status", "Stakeholder_Engagement",
        "Trend_Analysis", "Risk_Indicators", "Compliance_Evidence",
        "Monthly_Report", "Quarterly_Report", "Metadata"
    ]
    
    missing = [s for s in required if s not in wb.sheetnames]
    if missing:
        print(f"✗ FAIL: Missing sheets: {', '.join(missing)}")
        return False
    print("✓ All required sheets present")
    return True


def check_executive_summary_cvss(ws):
    """Validate Executive_Summary sheet has CVSS metrics."""
    print("\n  Checking Executive_Summary CVSS metrics...")
    
    issues = []
    
    # Check title
    title = ws["A1"].value
    if not title or "Executive Summary" not in title:
        issues.append("Dashboard title missing or incorrect")
    
    # Check for CVSS section header
    cvss_section_found = False
    cvss_adoption_found = False
    high_cvss_exploit_found = False
    cvss_severity_dist_found = False
    
    # Scan sheet for CVSS metrics
    for row in range(1, 60):
        for col in range(1, 13):
            cell_value = str(ws.cell(row=row, column=col).value or "")
            
            if "CVSS FRAMEWORK HEALTH" in cell_value.upper():
                cvss_section_found = True
            
            if "CVSS 4.0 Adoption Rate" in cell_value:
                cvss_adoption_found = True
            
            if "High CVSS + Active Exploitation" in cell_value or "EMERGENCY ACTION" in cell_value:
                high_cvss_exploit_found = True
            
            if "CVSS Severity Distribution" in cell_value or "Prevented Incidents by CVSS" in cell_value:
                cvss_severity_dist_found = True
    
    if not cvss_section_found:
        issues.append("CVSS Framework Health section not found")
    else:
        print("  ✓ CVSS Framework Health section present")
    
    if not cvss_adoption_found:
        issues.append("CVSS 4.0 Adoption Rate metric missing")
    else:
        print("  ✓ CVSS 4.0 Adoption Rate metric present")
    
    if not high_cvss_exploit_found:
        issues.append("High CVSS + Active Exploitation emergency indicator missing")
    else:
        print("  ✓ High CVSS + Active Exploitation indicator present (CRITICAL)")
    
    if not cvss_severity_dist_found:
        issues.append("CVSS Severity Distribution data/chart missing")
    else:
        print("  ✓ CVSS Severity Distribution present")
    
    if issues:
        for issue in issues:
            print(f"  ✗ {issue}")
        return False
    
    return True


def check_program_kpis_cvss(ws):
    """Validate Program_KPIs sheet has CVSS KPIs."""
    print("\n  Checking Program_KPIs CVSS KPIs...")
    
    # Check for CVSS KPI IDs
    required_cvss_kpis = [
        "KPI-CVSS-001",  # CVSS 4.0 Adoption Rate
        "KPI-CVSS-002",  # CVSS Accuracy Rate
        "KPI-CVSS-003",  # High CVSS + Active Exploitation
        "KPI-CVSS-004",  # Avg CVSS (Prevented)
        "KPI-RISK-001",  # Risk Updates with CVSS Quantification
        "KPI-INC-001",   # Incidents with CVSS Context
    ]
    
    found_kpis = []
    
    # Scan column A for KPI IDs (rows 5-40)
    for row in range(5, 41):
        kpi_id = ws[f"A{row}"].value
        if kpi_id in required_cvss_kpis:
            found_kpis.append(kpi_id)
            kpi_name = ws[f"C{row}"].value
            print(f"  ✓ Found {kpi_id}: {kpi_name}")
    
    missing = [kpi for kpi in required_cvss_kpis if kpi not in found_kpis]
    
    if missing:
        print(f"  ✗ Missing CVSS KPIs: {', '.join(missing)}")
        return False
    
    # Check KPI-CVSS-003 has emergency status formula
    for row in range(5, 41):
        if ws[f"A{row}"].value == "KPI-CVSS-003":
            status_col = ws[f"N{row}"].value
            if status_col and "EMERGENCY" in str(status_col).upper():
                print(f"  ✓ KPI-CVSS-003 has EMERGENCY status logic")
            else:
                print(f"  ⚠  KPI-CVSS-003 may be missing EMERGENCY status logic")
            break
    
    # Check for external references to CVSS columns
    cvss_external_refs = 0
    for row in range(5, 41):
        if ws[f"A{row}"].value in required_cvss_kpis:
            data_source = ws[f"G{row}"].value
            if data_source and ("CVSS" in str(data_source) or "Prevention_Tracking" in str(data_source)):
                cvss_external_refs += 1
    
    if cvss_external_refs >= 4:
        print(f"  ✓ CVSS KPIs have external reference formulas ({cvss_external_refs} found)")
    else:
        print(f"  ⚠  Only {cvss_external_refs} CVSS KPIs have external references (expected 4+)")
    
    return True


