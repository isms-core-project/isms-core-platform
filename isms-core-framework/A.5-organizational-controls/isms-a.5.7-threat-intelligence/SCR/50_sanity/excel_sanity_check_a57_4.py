def check_external_references(ws, sheet_name):
    """Check for external reference formulas in sheet."""
    external_refs = []
    
    for row in ws.iter_rows():
        for cell in row:
            if cell.value and isinstance(cell.value, str) and cell.value.startswith('='):
                formula = cell.value
                if "[ISMS-IMP-A.5.7." in formula:
                    # Extract workbook references
                    matches = re.findall(r'\[ISMS-IMP-A\.5\.7\.\d\.xlsx\]', formula)
                    external_refs.extend(matches)
    
    if external_refs:
        unique_refs = set(external_refs)
        print(f"  ✓ {sheet_name}: {len(external_refs)} external references to {len(unique_refs)} workbooks")
        return True
    else:
        print(f"  ⚠  {sheet_name}: No external references found")
        return False


