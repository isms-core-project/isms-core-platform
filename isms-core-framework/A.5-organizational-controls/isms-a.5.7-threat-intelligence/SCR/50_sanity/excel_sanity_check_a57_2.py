def check_vulnerability_linked_threats_v2(ws):
    """
    Validate Vulnerability_Linked_Threats sheet v1.0 (Sheet 8).
    CRITICAL: VTL schema with CVSS validation for Control 8.8 integration.
    """
    print("\n  Checking Vulnerability_Linked_Threats v1.0 (VTL + CVSS - CRITICAL)...")
    
    # Check title contains CVSS warning
    title = ws["A1"].value
    if not title or "CVSS" not in title:
        print(f"  ✗ CVSS integration not mentioned in title")
        return False
    
    # Check VTL IDs (100 capacity)
    vtl_ids = [ws[f"A{r}"].value for r in range(5, 105)]
    valid_ids = [vid for vid in vtl_ids if vid and vid.startswith("VTL-")]
    
    if len(valid_ids) < 50:
        print(f"  ✗ Expected at least 50 VTL ID slots, found {len(valid_ids)}")
        return False
    
    # Check CVSS columns present (columns 3, 4, 5)
    cvss_version_header = ws.cell(row=4, column=3).value
    cvss_score_header = ws.cell(row=4, column=4).value
    cvss_vector_header = ws.cell(row=4, column=5).value
    
    if not cvss_version_header or "CVSS" not in str(cvss_version_header).upper() or "VERSION" not in str(cvss_version_header).upper():
        print(f"  ✗ CVSS_Version column missing (expected column 3)")
        return False
    
    if not cvss_score_header or "CVSS" not in str(cvss_score_header).upper() or "SCORE" not in str(cvss_score_header).upper():
        print(f"  ✗ CVSS_Base_Score column missing (expected column 4)")
        return False
    
    if not cvss_vector_header or "CVSS" not in str(cvss_vector_header).upper() or "VECTOR" not in str(cvss_vector_header).upper():
        print(f"  ✗ CVSS_Vector column missing (expected column 5)")
        return False
    
    print(f"  ✓ CVSS columns present (Version, Base Score, Vector)")
    
    # Check Priority_Score is AUTO-CALCULATED (column 16, row 5 should have formula)
    priority_cell = ws.cell(row=5, column=16)
    priority_value = priority_cell.value
    
    if not priority_value or not isinstance(priority_value, str) or not priority_value.startswith("="):
        print(f"  ✗ Priority_Score not auto-calculated (expected formula in column 16)")
        return False
    
    # Check formula contains MIN and CVSS reference
    if "MIN" not in priority_value.upper() or "D5" not in priority_value:  # D5 = CVSS_Base_Score
        print(f"  ✗ Priority_Score formula incorrect (should be MIN(10, CVSS+...))")
        return False
    
    print(f"  ✓ Priority_Score AUTO-CALCULATED with CVSS formula")
    
    # Check conditional formatting present
    cf_rules = len(ws.conditional_formatting._cf_rules)
    if cf_rules < 4:  # Should have at least 4 CVSS rules
        print(f"  ⚠ Warning: Expected at least 4 CVSS conditional formatting rules, found {cf_rules}")
    else:
        print(f"  ✓ CVSS conditional formatting present ({cf_rules} rules)")
    
    # Check critical columns still present
    headers = [ws.cell(row=4, column=c).value for c in range(1, 25)]
    critical_headers = ["Exploitation Status", "Remediation Status", "Critical Assets Affected"]
    
    for ch in critical_headers:
        header_match = [h for h in headers if h and ch.upper() in str(h).upper()]
        if not header_match:
            print(f"  ✗ Critical VTL header missing: {ch}")
            return False
    
    # Check Last_Updated formula (auto TODAY)
    last_updated_formula = ws.cell(row=5, column=12).value
    if not last_updated_formula or "TODAY" not in str(last_updated_formula).upper():
        print(f"  ✗ Last_Updated auto-formula missing")
        return False
    
    print(f"  ✓ Vulnerability_Linked_Threats v1.0 validated (24 columns with CVSS)")
    print(f"  ✓ Control 8.8 integration with CVSS-based priority scoring")
    return True


