def check_merged_cells(wb):
    """Count merged cell ranges."""
    merged_count = 0
    
    for sheet_name in wb.sheetnames:
        ws = wb[sheet_name]
        merged_count += len(ws.merged_cells.ranges)
    
    return merged_count


