def normalize_colors(ws):
    """
    Normalize header colors across the worksheet.
    
    Only changes colors that need standardization.
    Preserves input cells, column headers, subheaders, etc.
    """
    changes_made = 0
    
    for row in ws.iter_rows():
        for cell in row:
            if cell.fill and cell.fill.start_color:
                current_color = str(cell.fill.start_color.rgb)[2:8] if hasattr(cell.fill.start_color, 'rgb') else None
                
                if not current_color:
                    current_color = cell.fill.start_color.index if hasattr(cell.fill.start_color, 'index') else None
                
                if current_color and current_color.upper() in COLOR_NORMALIZATION_MAP:
                    # This color needs normalization
                    new_color = COLOR_NORMALIZATION_MAP[current_color.upper()]
                    cell.fill = PatternFill(start_color=new_color, end_color=new_color, fill_type="solid")
                    changes_made += 1
    
    return changes_made


