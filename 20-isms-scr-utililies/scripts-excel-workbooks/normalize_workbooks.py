def normalize_row1_header(ws, control_full, title, control_name):
    """
    Normalize row 1 header to standard ISMS format.
    
    Standard format:
    ISMS-IMP-A.X.X.X — Title
    ISO/IEC 27001:2022 - Control A.X.X: Name
    
    Args:
        ws: Worksheet
        control_full: Full control (e.g., "A.8.24.1")
        title: Worksheet title (extracted from current header)
        control_name: Control category name
    """
    # Extract base control (A.X.X) from full control (A.X.X.X)
    base_control = ".".join(control_full.split(".")[:3]) if "." in control_full else control_full
    
    # Build standard header
    header_text = (
        f"ISMS-IMP-{control_full} — {title}\n"
        f"ISO/IEC 27001:2022 - Control {base_control}: {control_name}"
    )
    
    # Apply to A1 (assuming it's merged)
    ws["A1"] = header_text
    
    # Apply standard styling
    ws["A1"].font = Font(name="Calibri", size=14, bold=True, color=STANDARD_COLORS["header_font"])
    ws["A1"].fill = PatternFill(start_color=STANDARD_COLORS["header_bg"], 
                                 end_color=STANDARD_COLORS["header_bg"], 
                                 fill_type="solid")
    ws["A1"].alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
    
    # Set row height
    ws.row_dimensions[1].height = STANDARD_ROW1_HEIGHT


def extract_title_from_header(ws):
    """
    Extract the title from current A1 header.
    
    Tries to intelligently extract the meaningful title part.
    """
    try:
        current = str(ws["A1"].value) if ws["A1"].value else ""
        
        # Remove ISMS-IMP prefix if present
        current = re.sub(r'ISMS-IMP-A\.\d+\.\d+\.\d+\s*[-—]\s*', '', current)
        current = re.sub(r'ISMS-IMP-A\.\d+\.\d+\s*[-—]\s*', '', current)
        
        # Remove ISO/IEC reference if present
        current = re.sub(r'ISO/IEC 27001:20\d{2}.*', '', current)
        
        # Take first line if multi-line
        current = current.split('\n')[0].strip()
        
        # If still has control reference, clean it
        current = re.sub(r'\bA\.\d+\.\d+\b', '', current).strip()
        current = re.sub(r'Control\s+', '', current).strip()
        
        # Clean up extra separators
        current = current.strip(' -—')
        
        return current if current else "Assessment"
    except:
        return "Assessment"


