def normalize_workbook(input_path, output_path=None, dry_run=False, skip_non_isms=True):
    """
    Normalize a single Excel workbook.
    
    Args:
        input_path: Path to input workbook
        output_path: Path for output (default: same as input with _normalized suffix)
        dry_run: If True, only report what would be changed
        skip_non_isms: If True, skip files that don't appear to be ISMS workbooks
    
    Returns:
        dict: Summary of changes made
    """
    input_path = Path(input_path)
    
    if not input_path.exists():
        return {"error": "File not found", "path": str(input_path)}
    
    # Determine output path
    if output_path is None:
        output_path = input_path.parent / f"{input_path.stem}_normalized{input_path.suffix}"
    else:
        output_path = Path(output_path)
    
    print(f"\n{'[DRY RUN] ' if dry_run else ''}Normalizing: {input_path.name}")
    
    try:
        # Load workbook
        wb = load_workbook(input_path)
        
        # Try to detect control number
        filename_control = extract_control_from_filename(input_path.name)
        workbook_control = extract_control_from_workbook(wb)
        
        control_full = filename_control or workbook_control
        
        # Check if this looks like an ISMS workbook
        is_isms_workbook = (
            control_full is not None or
            'ISMS' in input_path.name.upper() or
            'A.' in input_path.name
        )
        
        if not is_isms_workbook and skip_non_isms:
            print(f"   ⏭️  Skipping: Doesn't appear to be an ISMS workbook")
            return {"skipped": True, "reason": "Not an ISMS workbook", "path": str(input_path)}
        
        if not control_full:
            print(f"   ⚠️  Could not auto-detect control number")
            if skip_non_isms:
                return {"skipped": True, "reason": "Control number not detected", "path": str(input_path)}
            else:
                return {"warning": "Control number not detected", "path": str(input_path)}
        
        print(f"   🔍 Detected control: {control_full}")
        
        # Extract base control for name lookup
        base_control = ".".join(control_full.split(".")[:3])
        control_name = CONTROL_NAMES.get(base_control, "Security Control")
        
        # Summary
        summary = {
            "file": input_path.name,
            "control": control_full,
            "sheets_processed": 0,
            "colors_normalized": 0,
            "headers_updated": 0,
        }
        
        # Process each worksheet
        for ws in wb.worksheets:
            # Extract current title
            title = extract_title_from_header(ws)
            
            # Normalize row 1 header (if it exists)
            try:
                if ws["A1"].value:
                    if not dry_run:
                        normalize_row1_header(ws, control_full, title, control_name)
                    summary["headers_updated"] += 1
            except:
                pass
            
            # Normalize colors
            if not dry_run:
                colors_changed = normalize_colors(ws)
                summary["colors_normalized"] += colors_changed
            
            summary["sheets_processed"] += 1
        
        # Save if not dry run
        if not dry_run:
            wb.save(output_path)
            print(f"   ✅ Saved to: {output_path.name}")
        else:
            print(f"   📋 Would save to: {output_path.name}")
        
        print(f"   📊 Sheets: {summary['sheets_processed']}, "
              f"Headers: {summary['headers_updated']}, "
              f"Colors: {summary['colors_normalized']}")
        
        return summary
    
    except Exception as e:
        print(f"   ❌ Error: {e}")
        return {"error": str(e), "path": str(input_path)}


def normalize_directory(directory, output_dir=None, pattern="*.xlsx", dry_run=False, backup=True, report_file=None, skip_non_isms=True):
    """
    Normalize all Excel workbooks in a directory.
    
    Args:
        directory: Directory to scan
        output_dir: Output directory (default: same as input)
        pattern: File pattern to match
        dry_run: If True, don't make changes
        backup: If True, create backups before normalizing
        report_file: Path for detailed report (default: auto-generated)
        skip_non_isms: If True, skip files that don't appear to be ISMS workbooks
    """
    directory = Path(directory)
    
    if not directory.exists():
        print(f"❌ Directory not found: {directory}")
        return
    
    # Find all matching files
    files = list(directory.rglob(pattern))
    
    if not files:
        print(f"❌ No files matching {pattern} found in {directory}")
        return
    
    # Auto-generate report filename if not provided
    if report_file is None:
        timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
        mode = "dryrun" if dry_run else "live"
        report_file = directory / f"normalization_report_{mode}_{timestamp}.txt"
    else:
        report_file = Path(report_file)
    
    print("=" * 80)
    print(f"ISMS WORKBOOK NORMALIZATION")
    print("=" * 80)
    print(f"Directory: {directory}")
    print(f"Files found: {len(files)}")
    print(f"Mode: {'DRY RUN' if dry_run else 'LIVE'}")
    print(f"Backup: {'Yes' if backup else 'No'}")
    print(f"Report: {report_file.name}")
    print("=" * 80)
    
    # Process each file
    results = []
    start_time = datetime.now()
    
    for i, file_path in enumerate(files, 1):
        print(f"\n[{i}/{len(files)}]", end=" ")
        
        # Create backup if requested
        backup_path = None
        if backup and not dry_run:
            backup_path = file_path.parent / f"{file_path.stem}_backup_{datetime.now().strftime('%Y%m%d_%H%M%S')}{file_path.suffix}"
            shutil.copy2(file_path, backup_path)
            print(f"💾 Backup: {backup_path.name}")
        
        # Determine output path
        if output_dir:
            out_path = Path(output_dir) / file_path.name
        else:
            out_path = file_path  # Overwrite original
        
        result = normalize_workbook(file_path, out_path, dry_run, skip_non_isms)
        result['backup_path'] = str(backup_path) if backup_path else None
        result['timestamp'] = datetime.now().isoformat()
        results.append(result)
    
    end_time = datetime.now()
    duration = (end_time - start_time).total_seconds()
    
    # Console Summary
    print("\n" + "=" * 80)
    print("NORMALIZATION SUMMARY")
    print("=" * 80)
    
    success = sum(1 for r in results if "error" not in r and "warning" not in r and "skipped" not in r)
    skipped = sum(1 for r in results if "skipped" in r)
    warnings = sum(1 for r in results if "warning" in r)
    errors = sum(1 for r in results if "error" in r)
    
    print(f"✅ Success: {success}")
    print(f"⏭️  Skipped: {skipped}")
    print(f"⚠️  Warnings: {warnings}")
    print(f"❌ Errors: {errors}")
    print(f"📊 Total: {len(results)}")
    print(f"⏱️  Duration: {duration:.1f}s")
    print(f"📄 Report: {report_file}")
    
    if skipped > 0:
        print(f"\nℹ️  {skipped} non-ISMS files skipped (use --pattern to be more specific)")
    
    if errors > 0:
        print("\nErrors:")
        for r in results:
            if "error" in r:
                print(f"   ❌ {r.get('path', 'unknown')}: {r['error']}")
    
    print("=" * 80)
    
    # Generate detailed report file
    generate_report(results, report_file, directory, pattern, dry_run, backup, start_time, end_time)
    
    return results


def generate_report(results, report_file, directory, pattern, dry_run, backup, start_time, end_time):
    """
    Generate a detailed normalization report file.
    
    Args:
        results: List of result dictionaries from normalization
        report_file: Path to save report
        directory: Directory that was processed
        pattern: File pattern used
        dry_run: Whether this was a dry run
        backup: Whether backups were created
        start_time: Start datetime
        end_time: End datetime
    """
    duration = (end_time - start_time).total_seconds()
    
    success = [r for r in results if "error" not in r and "warning" not in r and "skipped" not in r]
    skipped = [r for r in results if "skipped" in r]
    warnings = [r for r in results if "warning" in r]
    errors = [r for r in results if "error" in r]
    
    with open(report_file, 'w', encoding='utf-8') as f:
        # Header
        f.write("=" * 100 + "\n")
        f.write("ISMS WORKBOOK NORMALIZATION REPORT\n")
        f.write("=" * 100 + "\n")
        f.write(f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write(f"Mode: {'DRY RUN (no changes made)' if dry_run else 'LIVE (changes applied)'}\n")
        f.write(f"Directory: {directory}\n")
        f.write(f"Pattern: {pattern}\n")
        f.write(f"Backup Enabled: {'Yes' if backup else 'No'}\n")
        f.write(f"Start Time: {start_time.strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write(f"End Time: {end_time.strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write(f"Duration: {duration:.1f} seconds\n")
        f.write("=" * 100 + "\n\n")
        
        # Summary Statistics
        f.write("SUMMARY STATISTICS\n")
        f.write("-" * 100 + "\n")
        f.write(f"Total Files Processed:    {len(results)}\n")
        f.write(f"Successful:               {len(success)} ({len(success)/len(results)*100:.1f}%)\n")
        f.write(f"Skipped:                  {len(skipped)} ({len(skipped)/len(results)*100:.1f}%)\n")
        f.write(f"Warnings:                 {len(warnings)} ({len(warnings)/len(results)*100:.1f}%)\n")
        f.write(f"Errors:                   {len(errors)} ({len(errors)/len(results)*100:.1f}%)\n")
        f.write(f"Average Time per File:    {duration/len(results):.2f}s\n")
        f.write("-" * 100 + "\n\n")
        
        # Aggregate Statistics
        if success:
            total_sheets = sum(r.get('sheets_processed', 0) for r in success)
            total_headers = sum(r.get('headers_updated', 0) for r in success)
            total_colors = sum(r.get('colors_normalized', 0) for r in success)
            
            f.write("AGGREGATE CHANGES\n")
            f.write("-" * 100 + "\n")
            f.write(f"Total Sheets Processed:   {total_sheets}\n")
            f.write(f"Total Headers Updated:    {total_headers}\n")
            f.write(f"Total Colors Normalized:  {total_colors}\n")
            f.write("-" * 100 + "\n\n")
        
        # Control Detection Statistics
        controls_detected = [r for r in success if 'control' in r and r.get('control')]
        if controls_detected:
            control_counts = Counter()
            for r in controls_detected:
                control = r.get('control', '')
                if control and control != 'Unknown':
                    # Extract base control (A.X.X) from full control (A.X.X.X)
                    parts = control.split('.')
                    if len(parts) >= 3:
                        base_control = '.'.join(parts[:3])  # A.X.X
                        control_counts[base_control] += 1
            
            if control_counts:
                f.write("CONTROL DETECTION SUMMARY\n")
                f.write("-" * 100 + "\n")
                f.write(f"{'Control':<20} {'Count':<10}\n")
                f.write("-" * 100 + "\n")
                for control, count in sorted(control_counts.items()):
                    f.write(f"{control:<20} {count:<10}\n")
                f.write("-" * 100 + "\n\n")
        
        # Successful Files Details
        if success:
            f.write("SUCCESSFULLY PROCESSED FILES\n")
            f.write("=" * 100 + "\n\n")
            
            for i, result in enumerate(success, 1):
                f.write(f"[{i}] {result.get('file', 'unknown')}\n")
                f.write("-" * 100 + "\n")
