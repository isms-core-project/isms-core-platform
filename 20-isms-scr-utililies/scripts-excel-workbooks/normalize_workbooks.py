def extract_control_from_filename(filename):
    """
    Extract control number from generator script filename.
    
    Examples:
        generate_a523_1_inventory.py → A.5.23.1
        generate_a59_2_assets.py → A.5.9.2
        generate_a824_1_transmission.py → A.8.24.1
        generate_531_1_regulatory.py → A.5.31.1
        generate_iam_1_users.py → None (need docstring check)
    """
    patterns = [
        # Pattern: generate_aXYZ_N_*.py → A.X.YZ.N
        (r'generate_a(\d)(\d{2})_(\d+)_', lambda m: f"A.{m.group(1)}.{int(m.group(2))}.{m.group(3)}"),
        # Pattern: generate_XYZ_N_*.py → A.X.YZ.N (no 'a' prefix)
        (r'generate_(\d)(\d{2})_(\d+)_', lambda m: f"A.{m.group(1)}.{int(m.group(2))}.{m.group(3)}"),
    ]
    
    for pattern, formatter in patterns:
        match = re.search(pattern, filename)
        if match:
            return formatter(match)
    
    return None


def extract_control_from_workbook(wb):
    """
    Extract control number from workbook's first sheet docstring or header.
    
    Looks for patterns like:
    - ISMS-IMP-A.8.24.1
    - A.8.24
    - ISO/IEC 27001:2022 Control A.8.24
    """
    # Check first sheet, first few cells
    ws = wb.worksheets[0]
    
    # Check A1-A5 for control references
    for row in range(1, 6):
        for col in range(1, 4):
            try:
                cell = ws.cell(row=row, column=col)
                if cell.value:
                    text = str(cell.value)
                    
                    # Look for ISMS-IMP-A.X.X.X pattern
                    match = re.search(r'ISMS-IMP-A\.(\d+\.\d+)\.(\d+)', text)
                    if match:
                        return f"A.{match.group(1)}.{match.group(2)}"
                    
                    # Look for Control A.X.X pattern
                    match = re.search(r'Control\s+A\.(\d+\.\d+)', text)
                    if match:
                        return f"A.{match.group(1)}"
                    
                    # Look for A.X.X pattern
                    match = re.search(r'\bA\.(\d+\.\d+)\b', text)
                    if match:
                        return f"A.{match.group(1)}"
            except:
                continue
    
    return None


