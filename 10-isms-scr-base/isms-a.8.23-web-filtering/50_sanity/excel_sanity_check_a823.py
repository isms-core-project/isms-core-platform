def detect_workbook_type(filename):
    """Detect which A.8.23 workbook type this is based on filename."""
    filename_lower = filename.lower()
    
    if 'a.8.23.1' in filename_lower or 'filtering_infrastructure' in filename_lower:
        return 'A.8.23.1', 'Filtering Infrastructure Assessment'
    elif 'a.8.23.2' in filename_lower or 'network_coverage' in filename_lower:
        return 'A.8.23.2', 'Network Coverage Assessment'
    elif 'a.8.23.3' in filename_lower or 'policy_configuration' in filename_lower:
        return 'A.8.23.3', 'Policy Configuration Assessment'
    elif 'a.8.23.4' in filename_lower or 'monitoring_response' in filename_lower:
        return 'A.8.23.4', 'Monitoring & Response Assessment'
    elif 'a.8.23.5' in filename_lower or 'compliance_dashboard' in filename_lower:
        return 'A.8.23.5', 'Compliance Dashboard'
    else:
        return 'Unknown', 'Generic Excel Workbook'


