# ============================================================================

def check_workbook_health(filename):
    """Comprehensive diagnostic check for common openpyxl issues."""
    
    workbook_id, workbook_name = detect_workbook_type(filename)
    
    print("=" * 80)
    print(f"EXCEL WORKBOOK DIAGNOSTIC REPORT: {filename}")
    print(f"Detected Type: {workbook_id} - {workbook_name}")
    print("=" * 80)
    
    issues_found = []
    warnings_found = []
    
    try:
        wb = load_workbook(filename, data_only=False)
        print(f"\n✓ Workbook loaded successfully")
        print(f"  Sheets found: {len(wb.sheetnames)}")
        print(f"  Sheet names: {', '.join(wb.sheetnames)}")
        
    except Exception as e:
        print(f"\n✗ CRITICAL: Cannot load workbook: {e}")
        return
    
    # ========================================================================
    # CHECK 1: SHEET STRUCTURE VALIDATION
    # ========================================================================
    print("\n" + "=" * 80)
    print("CHECK 1: SHEET STRUCTURE VALIDATION")
    print("=" * 80)
    
    if workbook_id in EXPECTED_SHEETS:
        expected = EXPECTED_SHEETS[workbook_id]
        actual = wb.sheetnames
        
        # Check for missing sheets (flexible matching)
        missing = []
        for exp_sheet in expected:
            # Flexible match: check if expected sheet name is in any actual sheet
            found = False
            for act_sheet in actual:
                if exp_sheet.lower().replace("_", "").replace("&", "") in \
                   act_sheet.lower().replace("_", "").replace(" ", "").replace("&", ""):
                    found = True
                    break
            if not found:
                missing.append(exp_sheet)
        
        if missing:
            for sheet in missing:
                warnings_found.append(f"  ⚠ Missing expected sheet: {sheet}")
            print(f"  ⚠ {len(missing)} expected sheets not found (may be renamed)")
        else:
            print(f"  ✓ All expected sheets present for {workbook_id}")
        
        # Check for extra sheets
        print(f"  Total sheets: {len(actual)} (expected ~{len(expected)})")
    else:
        print("  ℹ Unknown workbook type - skipping sheet validation")
    
    # ========================================================================
    # CHECK 2: FORMULA VALIDATION
    # ========================================================================
    print("\n" + "=" * 80)
    print("CHECK 2: FORMULA VALIDATION")
    print("=" * 80)
    
    formula_issues = 0
    inter_sheet_refs = {}
    external_refs = set()
    
    for sheet_name in wb.sheetnames:
        ws = wb[sheet_name]
        for row in ws.iter_rows():
            for cell in row:
                if cell.value and isinstance(cell.value, str) and cell.value.startswith('='):
                    formula = cell.value
                    
                    # Check for external workbook references
                    ext_refs = re.findall(r'\[([^\]]+\.xlsx)\]', formula)
                    for ref in ext_refs:
                        external_refs.add(ref)
                    
                    # Check for sheet references
                    if "'" in formula or "!" in formula:
                        sheet_refs = re.findall(r"'?([^'!]+)'?!", formula)
                        for ref in sheet_refs:
                            ref_clean = ref.strip("'")
                            # Skip external references
                            if "[" not in ref_clean and ref_clean not in wb.sheetnames:
                                # Check for close matches (underscore vs space)
                                close_match = False
                                for actual in wb.sheetnames:
                                    if ref_clean.replace(" ", "_") == actual or \
                                       ref_clean.replace("_", " ") == actual or \
                                       ref_clean.replace("&", "") == actual.replace("&", ""):
                                        close_match = True
                                        break
                                if not close_match:
                                    issues_found.append(
                                        f"  ✗ {sheet_name}!{cell.coordinate}: "
                                        f"Invalid sheet reference '{ref_clean}'"
                                    )
                                    formula_issues += 1
                            
                            # Track inter-sheet references
                            if ref_clean in wb.sheetnames or close_match:
                                if sheet_name not in inter_sheet_refs:
                                    inter_sheet_refs[sheet_name] = set()
                                inter_sheet_refs[sheet_name].add(ref_clean)
                    
                    # Check for unbalanced quotes and parentheses
                    if formula.count("'") % 2 != 0:
                        issues_found.append(
                            f"  ✗ {sheet_name}!{cell.coordinate}: Unbalanced single quotes"
                        )
                        formula_issues += 1
                    
                    if formula.count("(") != formula.count(")"):
                        issues_found.append(
                            f"  ✗ {sheet_name}!{cell.coordinate}: Unbalanced parentheses"
                        )
                        formula_issues += 1
    
    if formula_issues == 0:
        print("  ✓ All formulas appear valid")
    else:
        print(f"  ⚠ Found {formula_issues} potential formula issues")
    
    if inter_sheet_refs:
        print(f"\n  Inter-sheet references found in {len(inter_sheet_refs)} sheets")
        for sheet, refs in inter_sheet_refs.items():
            print(f"    {sheet} → {', '.join(list(refs)[:3])}" + 
                  ("..." if len(refs) > 3 else ""))
    
    if external_refs:
        print(f"\n  External workbook references: {len(external_refs)}")
        for ref in list(external_refs)[:5]:
            print(f"    • {ref}")
        if len(external_refs) > 5:
            print(f"    ... and {len(external_refs) - 5} more")
    
    # ========================================================================
    # CHECK 3: DATA VALIDATION CONFLICTS
    # ========================================================================
    print("\n" + "=" * 80)
    print("CHECK 3: DATA VALIDATION CONFLICTS")
    print("=" * 80)
    
    validation_issues = 0
    for sheet_name in wb.sheetnames:
        ws = wb[sheet_name]
        
        # Check for overlapping data validations
        validations = list(ws.data_validations.dataValidation)
        if len(validations) > 1:
            for i, dv1 in enumerate(validations):
                for dv2 in validations[i+1:]:
                    # Both have ranges, check for overlap
                    if hasattr(dv1, 'sqref') and hasattr(dv2, 'sqref'):
                        # Simple overlap detection (could be improved)
                        ranges1 = str(dv1.sqref).split()
                        ranges2 = str(dv2.sqref).split()
                        
                        for r1 in ranges1:
                            for r2 in ranges2:
                                if r1 == r2:
                                    warnings_found.append(
                                        f"  ⚠ {sheet_name}: Overlapping data validation ranges: {r1}"
                                    )
                                    validation_issues += 1
    
    if validation_issues == 0:
        print("  ✓ No data validation conflicts detected")
    else:
        print(f"  ⚠ Found {validation_issues} potential validation conflicts")
    
    # ========================================================================
    # CHECK 4: MERGED CELL VALIDATION
    # ========================================================================
    print("\n" + "=" * 80)
    print("CHECK 4: MERGED CELL VALIDATION")
    print("=" * 80)
    
    merge_issues = 0
    for sheet_name in wb.sheetnames:
        ws = wb[sheet_name]
        
        for merged_range in ws.merged_cells.ranges:
            # Check if any cell other than top-left has content
            min_row, min_col, max_row, max_col = merged_range.bounds
            
            for row in range(min_row, max_row + 1):
                for col in range(min_col, max_col + 1):
                    # Skip top-left cell
                    if row == min_row and col == min_col:
                        continue
                    
                    cell = ws.cell(row, col)
                    if cell.value is not None:
                        issues_found.append(
                            f"  ✗ {sheet_name}!{cell.coordinate}: "
                            f"Content in merged cell (not top-left)"
                        )
                        merge_issues += 1
    
    if merge_issues == 0:
        print("  ✓ All merged cells properly formatted")
    else:
        print(f"  ⚠ Found {merge_issues} merged cell issues")
    
    # ========================================================================
    # CHECK 5: WORKBOOK PROPERTIES AND METADATA
    # ========================================================================
    print("\n" + "=" * 80)
    print("CHECK 5: WORKBOOK PROPERTIES")
    print("=" * 80)
    
    props = wb.properties
    if props.creator:
        print(f"  Creator: {props.creator}")
    if props.lastModifiedBy:
        print(f"  Last Modified By: {props.lastModifiedBy}")
    if props.created:
        print(f"  Created: {props.created}")
    if props.modified:
        print(f"  Modified: {props.modified}")
    
    # Check for suspiciously old modification dates
    if props.modified:
        from datetime import datetime
        age_days = (datetime.now() - props.modified).days
        if age_days > 365:
            warnings_found.append(f"  ⚠ Workbook last modified {age_days} days ago")
    
    # ========================================================================
    # CHECK 6: NAMED RANGES VALIDATION
    # ========================================================================
    print("\n" + "=" * 80)
    print("CHECK 6: NAMED RANGES VALIDATION")
    print("=" * 80)
    
    named_ranges = wb.defined_names
    if len(named_ranges) > 0:
        print(f"  Found {len(named_ranges)} named ranges")
        for name, defn in list(named_ranges.items())[:5]:
            print(f"    • {name}: {defn.attr_text[:50]}...")
        if len(named_ranges) > 5:
            print(f"    ... and {len(named_ranges) - 5} more")
        
        # Check for broken named range references
        broken_ranges = 0
        for name, defn in named_ranges.items():
            if defn.attr_text:
                # Check if it references a non-existent sheet
                if "!" in defn.attr_text:
                    sheet_ref = defn.attr_text.split("!")[0].strip("'")
                    if sheet_ref not in wb.sheetnames:
                        warnings_found.append(
                            f"  ⚠ Named range '{name}' references non-existent sheet '{sheet_ref}'"
                        )
                        broken_ranges += 1
        
        if broken_ranges > 0:
            print(f"  ⚠ Found {broken_ranges} potentially broken named ranges")
    else:
        print("  No named ranges defined")
    
