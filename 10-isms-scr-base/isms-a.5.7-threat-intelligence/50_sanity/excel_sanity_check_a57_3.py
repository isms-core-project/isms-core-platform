    print("CHECK 2: TOOL_INTEGRATION_MATRIX VALIDATION")
    print("=" * 80)
    
    if "Tool_Integration_Matrix" in wb.sheetnames:
        ws = wb["Tool_Integration_Matrix"]
        
        # Check for 30 tool rows (A5:A34)
        tool_count = 0
        for row in range(5, 35):
            if ws[f"A{row}"].value and str(ws[f"A{row}"].value).startswith("TOOL-"):
                tool_count += 1
        
        if tool_count == 30:
            print(f"  ✓ Tool_Integration_Matrix: 30 tool rows")
        else:
            warnings_found.append(f"  ⚠  Tool_Integration_Matrix: {tool_count}/30 tools")
        
        # Check for conditional formatting (integration status colors)
        cf_rules = ws.conditional_formatting._cf_rules
        if cf_rules:
            print(f"  ✓ Tool_Integration_Matrix: Conditional formatting present")
        else:
            warnings_found.append(f"  ⚠  Tool_Integration_Matrix: No conditional formatting")
    
