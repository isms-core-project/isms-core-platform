    print("CHECK 1: SHEET STRUCTURE")
    print("=" * 80)
    
    if len(wb.sheetnames) != 15:
        issues_found.append(f"  ✗ Expected 15 sheets, found {len(wb.sheetnames)}")
    else:
        print(f"  ✓ Sheet count: 15")
    
